package Index;

/**
 * Клас Item (Предмет/Бонус).
 * Це клас, який описує об'єкт на карті, що можна підібрати (наприклад, золотий злиток).
 * * Він дуже простий і містить лише координати.
 * Вся логіка того, як він виглядає (текстура), знаходиться в класі Render3D.
 * Вся логіка того, що стається, коли його підбирають (очки), знаходиться в класі Game.
 */
public class Item {

    /**
     * Координата X предмета у світі.
     * Ми використовуємо тип double (число з комою), а не int (ціле число).
     * Це дозволяє розміщувати предмети не строго по сітці (наприклад, 5.0),
     * а де завгодно (наприклад, 5.75), що робить світ більш живим.
     */
    public double x;

    /**
     * Координата Z предмета у світі (глибина).
     * У 3D-движках зазвичай:
     * - X та Z — це площина землі (ліво-право, вперед-назад).
     * - Y — це висота (вгору-вниз).
     * Оскільки у нас предмети завжди лежать на підлозі, координата Y нам тут не потрібна.
     */
    public double z;

    /**
     * Конструктор Item.
     * Створює новий предмет у заданій точці.
     *
     * @param x Позиція по осі X.
     * @param z Позиція по осі Z.
     */
    public Item(double x, double z) {
        this.x = x;
        this.z = z;
    }
}