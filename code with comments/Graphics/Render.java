package Graphics;

// Імпортуємо клас Display, щоб мати доступ до розмірів екрана (Display.WIDTH та Display.HEIGHT)
import Index.Display;

/**
 * Клас Render (Рендер/Відображувач).
 * Цей клас відповідає за обробку графіки. Він являє собою "полотно" з пікселів,
 * на якому ми можемо малювати.
 */
public class Render {

    /**
     * Ширина області рендерингу (в пікселях).
     */
    public final int width;

    /**
     * Висота області рендерингу (в пікселях).
     */
    public final int height;

    /**
     * Головний масив пікселів.
     * Це одновимірний масив, який зберігає дані про колір кожного пікселя на екрані.
     * Розмір масиву = ширина * висота.
     */
    public final int[] pixels;

    /**
     * Конструктор класу Render.
     * Ініціалізує об'єкт рендера з заданими розмірами та створює порожній масив для пікселів.
     *
     * @param width  Бажана ширина області малювання.
     * @param height Бажана висота області малювання.
     */
    public Render(int width, int height) {
        this.width = width;
        this.height = height;
        // Створюємо масив цілих чисел. Кількість елементів дорівнює площі екрана.
        pixels = new int[width * height];
    }

    /**
     * Метод draw (Малювати).
     * Ця функція копіює пікселі з іншого об'єкта Render (наприклад, спрайта або текстури)
     * на поточний екран ("полотно") у вказані координати.
     *
     * @param render  Об'єкт Render, який ми хочемо намалювати (джерело зображення).
     * @param xOffset Зміщення по осі X (координата, де почнеться малювання).
     * @param yOffset Зміщення по осі Y (координата, де почнеться малювання).
     */
    public void draw(Render render, int xOffset, int yOffset) {

        // Проходимо циклом по кожному рядку (висоті) зображення, яке малюємо
        for (int y = 0; y < render.height; y++) {

            // Розраховуємо абсолютну позицію пікселя по Y на екрані
            int yPix = y + yOffset;

            // Перевірка меж екрана по вертикалі:
            // Якщо піксель виходить за верхню (< 0) або нижню (>= Display.HEIGHT) межу екрана,
            // ми його пропускаємо (не малюємо), щоб уникнути помилок.
            if (yPix < 0 || yPix >= Display.HEIGHT) {
                continue;
            }

            // Проходимо циклом по кожному пікселю в рядку (ширині) зображення
            for (int x = 0; x < render.width; x++) {

                // Розраховуємо абсолютну позицію пікселя по X на екрані
                int xPix = x + xOffset;

                // Перевірка меж екрана по горизонталі:
                // Якщо піксель виходить за ліву (< 0) або праву (>= Display.WIDTH) межу екрана,
                // ми переходимо до наступного пікселя.
                if (xPix < 0 || xPix >= Display.WIDTH) {
                    continue;
                }

                // Отримуємо колір поточного пікселя з об'єкта 'render' (джерела).
                // Формула (x + y * render.width) перетворює 2D координати в індекс 1D масиву.
                int alpha = render.pixels[x + y * render.width];

                // Перевірка на прозорість.
                // Якщо значення кольору (alpha) більше 0, то ми малюємо піксель.
                // Це дозволяє робити прозорий фон у спрайтів (0 вважається "порожнім" кольором).
                if (alpha > 0) {
                    // Записуємо колір у масив пікселів головного екрана.
                    pixels[xPix + yPix * width] = alpha;
                }
            }
        }
    }
}